  created_at      timestamptz not null default now(),
  updated_at      timestamptz not null default now(),
  constraint memberships_short_term_window check (
    plan_kind <> 'short_term' or (starts_at is not null and ends_at is not null and ends_at > starts_at)
  ),
  constraint memberships_long_term_window check (
    plan_kind <> 'long_term' or (valid_from is not null and valid_until is not null and valid_until >= valid_from)
  ),
  constraint memberships_seat_no_overlap_short_term exclude using gist (
    seat_number with =,
    tstzrange(starts_at, ends_at, '[)') with &&
  ) where (status = 'active' and plan_kind = 'short_term' and seat_number is not null and deleted_at is null),
  constraint memberships_seat_no_overlap_long_term exclude using gist (
    seat_number with =,
    daterange(valid_from, (valid_until + 1), '[)') with &&
  ) where (status = 'active' and plan_kind = 'long_term' and seat_number is not null and deleted_at is null)
);

