-- =============================================================================
-- Mani Library — ORGANIZED SCHEMA v2 (greenfield)
-- =============================================================================
-- Run in an EMPTY Supabase project (SQL Editor) after `auth` exists.
--
-- DESIGN GOALS (per product spec)
--   • One clear “core” set: profiles, memberships, payments, verification.
--   • “Active *” are VIEWS only (no duplicate membership/payment tables).
--   • KYC files: normalized table `verification_documents` (not JSON blobs).
--   • Device vendor APIs: ONE ingest table `device_api_records` (+ thin map).
--   • CSV / manual member onboarding: `member_manual_import` (staging).
--   • `device_user_id`: identity 1111–9999, immutable on profile; maps to eTime
--     `empcode` via `etime_empcode_map` for the face device.
--   • Sensitive columns: `admin_internal_note_cipher`, optional `profile_pii_cipher`
--     using `encrypt_note` / `decrypt_note` (pgcrypto; passphrase via session or Vault).
--   • RLS on every base table; service_role bypasses RLS for server jobs.
--
-- TABLE COUNT (physical base tables): 12
--   1 profiles
--   2 verification
--   3 verification_documents
--   4 memberships
--   5 payments
--   6 membership_events
--   7 attendance_days
--   8 library_export_audit
--   9 device_api_records          ← replaces etime_punch_raw + etime_attendance_daily
--  10 etime_empcode_map           ← empcode (0001) ↔ device_user_id
--  11 member_manual_import        ← CSV / manual rows before Auth user exists
--  12 device_api_sync_state       ← LastRecord / incremental cursors per empcode
--
-- VIEWS (not counted as tables): 5
--   device_lookup, active_profiles, active_memberships, active_payments,
--   active_verification_documents
--
-- BREAKING vs `new-database-schema-rls-encryption.sql`:
--   • App must read/write `verification_documents` instead of `verification.documents` jsonb.
--   • App must read/write `device_api_records` instead of `etime_punch_raw` / `etime_attendance_daily`.
-- =============================================================================

create extension if not exists pgcrypto;
create extension if not exists btree_gist;

-- ---------------------------------------------------------------------------
-- Encryption helpers (set `app.library_passphrase` in session for encrypt;
-- production: wrap with Vault — see old schema comments.)
-- ---------------------------------------------------------------------------
create or replace function public.encrypt_note(p_plain text)
returns bytea
language sql
stable
as $$
  select case
    when p_plain is null then null::bytea
    when nullif(trim(current_setting('app.library_passphrase', true)), '') is null then null::bytea
    else pgp_sym_encrypt(p_plain, current_setting('app.library_passphrase'), 'cipher-algo=aes256')
  end;
$$;

create or replace function public.decrypt_note(p_secret bytea)
returns text
language sql
stable
as $$
  select case
    when p_secret is null then null::text
    when nullif(trim(current_setting('app.library_passphrase', true)), '') is null then null::text
    else pgp_sym_decrypt(p_secret, current_setting('app.library_passphrase'))::text
  end;
$$;

revoke all on function public.encrypt_note(text) from public;
revoke all on function public.decrypt_note(bytea) from public;
grant execute on function public.encrypt_note(text) to service_role;
grant execute on function public.decrypt_note(bytea) to service_role;

