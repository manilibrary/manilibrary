create or replace function public.memberships_log_event()
returns trigger language plpgsql security definer set search_path = public set row_security = off as $$
declare
  uid uuid := auth.uid();
  r text := case
  when coalesce((select p.is_superadmin from public.profiles p where p.user_id = uid and p.deleted_at is null), false) then 'superadmin'
  when coalesce((select p.is_admin from public.profiles p where p.user_id = uid and p.deleted_at is null), false) then 'admin'
  when uid is not null then 'member' else 'system' end;
begin
  if tg_op = 'INSERT' then
    insert into public.membership_events (membership_id, user_id, event_type, new_status, changed_by, changed_by_role)
    values (new.id, new.user_id, 'created', new.status, uid, r);
    return new;
  end if;
  if tg_op = 'UPDATE' then
    if new.deleted_at is not null and old.deleted_at is null then
      insert into public.membership_events (membership_id, user_id, event_type, old_status, new_status, changed_by, changed_by_role)
      values (new.id, new.user_id, 'soft_deleted', old.status, new.status, uid, r);
    elsif new.status is distinct from old.status then
      insert into public.membership_events (membership_id, user_id, event_type, old_status, new_status, changed_by, changed_by_role)
      values (new.id, new.user_id, 'updated', old.status, new.status, uid, r);
    end if;
  end if;
  return new;
end;
$$;

create trigger trg_memberships_log_event_aiu after insert or update on public.memberships
  for each row execute function public.memberships_log_event();

create or replace function public.memberships_log_event_delete()
returns trigger language plpgsql security definer set search_path = public set row_security = off as $$
declare
  uid uuid := auth.uid();
  r text := case
  when coalesce((select p.is_superadmin from public.profiles p where p.user_id = uid and p.deleted_at is null), false) then 'superadmin'
  when coalesce((select p.is_admin from public.profiles p where p.user_id = uid and p.deleted_at is null), false) then 'admin'
  when uid is not null then 'member' else 'system' end;
begin
  insert into public.membership_events (membership_id, user_id, event_type, old_status, changed_by, changed_by_role)
  values (old.id, old.user_id, 'hard_deleted', old.status, uid, r);
  return old;
end;
$$;

create trigger trg_memberships_log_event_del before delete on public.memberships
  for each row execute function public.memberships_log_event_delete();

