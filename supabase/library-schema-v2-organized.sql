create or replace function public.sync_profile_is_verified_from_verification()
returns trigger language plpgsql security definer set search_path = public set row_security = off as $$
begin
  if tg_op = 'UPDATE' and new.status is distinct from old.status then
    if new.status = 'approved' then
      update public.profiles set is_verified = true, updated_at = now() where user_id = new.user_id;
    elsif new.status in ('rejected', 'resubmit', 'pending') then
      update public.profiles set is_verified = false, updated_at = now() where user_id = new.user_id;
    end if;
  end if;
  return new;
end;
$$;

create trigger trg_sync_profile_is_verified after update on public.verification
  for each row execute function public.sync_profile_is_verified_from_verification();

create trigger trg_verification_updated before update on public.verification
  for each row execute function public.touch_updated_at();

create or replace function public.verification_guard_member_updates()
returns trigger language plpgsql security definer set search_path = public set row_security = off as $$
begin
  if tg_op = 'UPDATE' and new.user_id = auth.uid()
     and not coalesce((select p.is_admin from public.profiles p where p.user_id = auth.uid() and p.deleted_at is null), false)
     and not coalesce((select p.is_superadmin from public.profiles p where p.user_id = auth.uid() and p.deleted_at is null), false) then
    if new.status is distinct from old.status
       or new.reviewed_at is distinct from old.reviewed_at
       or new.reviewed_by is distinct from old.reviewed_by
       or new.admin_internal_note_cipher is distinct from old.admin_internal_note_cipher
       or new.deleted_at is distinct from old.deleted_at
       or new.deleted_by is distinct from old.deleted_by
       or new.submitted_at is distinct from old.submitted_at then
      raise exception 'members may only update student_message or resubmit_count while pending (use verification_documents for files)';
    end if;
  end if;
  return new;
end;
$$;

create trigger trg_verification_guard_member before update on public.verification
  for each row execute function public.verification_guard_member_updates();

